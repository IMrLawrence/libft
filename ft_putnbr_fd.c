#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int	np;

	if (n < 0)
	{
		write(fd, "-", 1);
		if (n == -2147483648LL)
		{
			write(fd, "2", 1);
			n = -147483648;
		}
		n *= -1;
	}
	if (n / 10)
		ft_putnbr_fd((n / 10), fd);
	np = (n % 10) + '0';
	write(fd, &np, 1);
}
